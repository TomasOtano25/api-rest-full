<?php

namespace App\Mail;

use Illuminate\Bus\Queueable;
use Illuminate\Mail\Mailable;
use Illuminate\Queue\SerializesModels;
use Illuminate\Contracts\Queue\ShouldQueue;
use App\Models\User;

class UserCreated extends Mailable
{
    use Queueable, SerializesModels;

    public $user;

    public function __construct(User $user)
    {
        $this->user = $user;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        // return $this->view('view.name');
        // return $this->text('emails.welcome')->subject('Por favor confirmar su correo electronico'); //->with(['user', $user]);
        return $this->markdown('emails.welcome')->subject('Por favor confirmar su correo electronico'); //->with(['user', $user]);
    }
}
